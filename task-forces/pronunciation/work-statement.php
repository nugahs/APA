		<h1>Spoken Presentation Task Force Work Statement</h1>
		<p>The Spoken Presentation Task Force is a task force of the <a href="http://www.w3.org/WAI/APA/">Accessible Platform Architectures Working Group (APA Working Group)</a>. It assists this Working Group with the work identified below.</p>
		<section id="status">
			<h2>Status</h2>
			<p>The Spoken Pronunciation Task Force was <a href="https://lists.w3.org/Archives/Public/public-apa-admin/2018Oct/0008.html">approved on 10 June 2016</a> and is now active. The <a href="https://www.w3.org/WAI/APA/task-forces/pronunciation/">Task Force home page</a> contains information about the operation and resources of the group.</p>
		</section>
		<section id="objective">
			<h2>Objective</h2>
			<p>The objective of the Spoken Presentation Task Force is to develop normative specifications and best practices guidance collaborating with other W3C groups as appropriate, to provide for proper pronunciation in HTML content when using text to speech (TTS) synthesis. The Task Force will bring together participants with expertise in accessibility, TTS technology, assistive technology use of TTS, and relevant technologies developed by the W3C in order to develop a workable solution to support accurate and consistent pronunciation of content. The Task Force will:</p>
			<ul>
				<li> Develop and analyze use cases and requirements;</li>
				<li>Research existing techniques and develop a Gap Analysis;</li>
				<li>Evaluate and develop a solution in the context of existing W3C technologies, most probably based on SSML (Speech Synthesis Markup Language);</li>
				<li>Identify related work inside and outside of W3C;</li>
				<li>Propose and develop a consistent mechanism to incorporate SSML into HTML using, for example, WAI-ARIA attributes, new attribute(s) for HTML, or an HTML element;</li>
				<li>Review issues raised against any related specification and propose solutions to issues which have an impact on the chosen approach;</li>
				<li>Respond to requests and provide expert feedback on accessibility and technical challenges;</li>
				<li>Build a use case repository to describe different scenarios involving pronunciation issues occurring in various languages;</li>
				<li>Engage stakeholders including assistive technology and browser vendors, from whom the task force can solicit ongoing feedback;</li>
				<li>The task force may form sub-teams to carry out work of the task force.</li>
			</ul>
		</section>
		<section id="scope">
			<h2>Scope of Work</h2>
			<p>The Spoken Presentation Task Force will develop:</p>
			<ol>
				<li>Gap analysis;</li>
				<li>Use cases and requirements document;</li>
				<li>A document proposing a technical approach for providing the solution to address the need for accurate pronunciation and presentation of spoken content in various languages.</li>
			</ol>
		</section>
		<section id="approach">
			<h2>Approach</h2>
			<p>The SPTF works within the APA Working Group and will focus on achieving a specific goal that will be broadly implementable by browsers, and will work with the <a href="https://www.w3.org/publishing/groups/publ-wg/">Publishing Working Group</a>, <a href="http://www.w3.org/WebPlatform/WG/">Web Platform Working Group</a>, <a href="https://www.w3.org/WAI/ARIA/">ARIA Working Group</a>, <a href="https://www.w3.org/Style/CSS/">CSS Working Group</a>, and <a href="http://www.w3.org/AudioVideo/TT/">Timed Text Working Group</a>. The Task Force will monitor new issues raised against its approach and related specifications and provide feedback on those impacting accessibility. </p>
		</section>
