		<h1>[DRAFT] Payments Accessibility Task Force Work Statement</h1>
		<p>The Payments Accessibility Task Force is a task force of the <a href="/WAI/APA/">Accessible Platform Architectures Working Group (APA WG)</a> and the <a href="http://www.w3.org/Payments/IG/">Web Payments Interest Group</a>. It assists this Working Group with the work identified below.</p>
		<section>
			<h2>Status</h2>
			<p class="note">This is a draft task force work statement. It does not have standing at this time.</p>
		</section>
		<section>
			<h2>Objective</h2>
			<p>The mission of the Payments Accessibility Task Force is to focus effort on identifying accessibility issues with the new technology of Web Payments. The task force will comprehensively review deliverables of the <a href="/Payments/WG/">Web Payments Working Group</a> and coordinate with that group on proposed solutions.</p>
		</section>
		<section>
			<h2>Scope of Work</h2>
			<p>The Payments <abbr title="accessibility">A11Y</abbr> <abbr title="task force">TF</abbr> will:</p>
			<ul>
				<li>Review feature sets provided by the Web Payments specifications to determine potential impacts on accessibility;</li>
				<li>Develop techniques and best practices where feasible to guide authors to create accessible Payments usages;</li>
				<li>Create concrete guidance about how user agents should expose accessibility information in Payments-styled content to assistive technologies;</li>
				<li>Work with the Payments, APA, and other Working Groups if needed to provide additional technical features to close accessibility gaps.</li>
			</ul>
		</section>
		<section>
			<h2>Approach</h2>
			<p>The Payments A11Y TF will:</p>
			<ul>
				<li>Develop informal research notes about issues within its scope, most likely in a wiki or GitHub repository but potentially to be published by the APA WG as Working Group Notes;</li>
				<li>Publish, jointy via the APA, ARIA, and Payments WGs, non-normative authoring guidance about Payments accessibility;</li>
			</ul>
		</section>
		<section>
			<h2>Communication</h2>
			<p>Payments Accessibility TF communications and discussions are visible to the public. The <a href=".">task force home page</a> details communication mechanisms in use. The task force will:</p>
			<ul>
				<li>Support synchronous (e.g., teleconferences, face-to-face meetings) and asynchronous (e.g., mailing list discussions, issue trackers, wiki, surveys, etc.) methods of communication;
				<li>Use the procedures of the <a href="https://www.w3.org/WAI/APA/decision-policy">APA Working Group Decision Policy</a> to make decisions;
				<li>Provide updates and feedback to the APA, ARIA, and Payments Working Group teleconferences, as appropriate. These liaisons are primarily the responsibility of the Payments A11Y TF Facilitators;
				<li>Maintain information resulting from its activities (deliverables, participants, face-to-face meetings, teleconferences, etc.) on the <a href=".">Payments A11Y TF home page</a>.
			</ul>
		</section>
		<section>
			<h2 id="participation">Participation</h2>
			<p>Any participant in the APA WG may participate in the Payments Accessibility TF. Participants should expect to dedicate 3 to 4 hours per week for Payments Accessibility TF work, which is considered part of the overall work commitment to the sponsoring WG: </p>
			<ul>
				<li>Remain current on the Payments Accessibility TF mailing list and respond to postings in a timely manner; </li>
				<li>Provide feedback to editors of specifications via email or by filing bugs; </li>
				<li>Participate in weekly Payments Accessibility TF teleconferences, or send regrets to the Payments Accessibility TF mailing list. </li>
			</ul>
			<p>Participants may also join Payments Accessibility TF sub-groups. Sub-groups take on specific assignments for the Payments Accessibility TF. </p>
			<p>If you are interested in becoming a participant of the Payments Accessibility TF or have any questions regarding its work, contact the task force facilitators. </p>
